# ТИП ДАНЫХ СПИСОК (продолжение)

text = 'Hello World'
text = list(text) # разделит список на отдельные элементы, даже пробел и превратит его в строку

print(text)

text = ''.join(text) # метод join заменяет разделители между элементами на указаный вернет Hello World вернули обратно
# список в строку
print(text)


