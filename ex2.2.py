# ТИП ДАННЫХ СЛОВАРИ

person = {'name': 'Tom', 'age': 56} # задаем ключи в словоре самостоятельно имя и возраст

print(person['name'])

person['cars'] = ['ferrari', 'zaz'] # добавили новый ключ и значения в список (БД)
print(person)

person['name'] = 'Steve' # поменяли в ключе значение
print(person)
